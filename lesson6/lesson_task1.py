# Необхідно зчитати рядок з 5 цифр, розділених пропусками, і зберегти кожну цифру у список.
# Створіть копію списку із впорядкованими елементами у зворотному порядку.
# Виведіть число, яке утворюється об'єднанням елементів нового списку.
list_ = input('Enter 5 digits: ').split(' ')
copy = list_.copy()
copy.sort(reverse=True)
print(''.join(copy))
